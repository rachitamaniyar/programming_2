package hotel;

public class Guest {
    private Key key;
    private String name;

    public Guest(String name) {
        this.key = key;
        this.name = name;
    }

    public Key getKey() {
        return key;
    }

    public void setKey(Key key) {
        this.key = key;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
